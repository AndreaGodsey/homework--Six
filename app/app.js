function loadData() {
  $.each(camping, function (idx, camping){
    $("#app").append(`<div id="${idx}" <div class="sale-holder">
    <h4>${camping.saleTitle}</h4>
    <div class="sale-image">
      <img src="images/${camping.saleThumbImg}" alt="${camping.saleTitle}" />
    </div>
    <div class="sale-camping">
      <div class="sale-des">
        ${camping.saleBriefDescription}
      </div>
      <div class="sale-price">Sale Price: ${camping.salePrice}</div>
    </div>
  </div>`);
  });
  initListeners();
}

function initListeners() {
    $(".sale-holder").click(function (e){
        let saleIndex = e.currentTarget.id;
        $("#app").html(`<div class="sale-item">
        <h2>${camping[saleIndex].saleTitle}</h2>
        <div class="sale-image-linked">
          <img src="images/${camping[saleIndex].saleThumbImg}" alt="tent" />
        </div>
        <div class="sale-des-linked">
        ${camping[saleIndex].saleDescription}
        </div>
        <div class="sale-rating">${camping[saleIndex].salePrice}</div>
        <div class="close">CLOSE</div>`);


        addCloseListener();
    });
}


function addCloseListener(){
    $(".close").click(function () {
        $("#app").html("");
        loadData();
    });
}



$(document).ready(function () {
     loadData();
});